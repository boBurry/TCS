// ── i18n engine ───────────────────────────────────────────────────────────────
window.i18n = {
  current: localStorage.getItem("tcs_lang") || "en",

  t(key) {
    return (TRANSLATIONS[this.current] || TRANSLATIONS.en)[key] || key;
  },

  setLang(lang) {
    this.current = lang;
    localStorage.setItem("tcs_lang", lang);
    this.apply();
    // Update switcher buttons everywhere
    document.querySelectorAll(".lang-btn").forEach((btn) => {
      btn.classList.toggle("lang-active", btn.dataset.lang === lang);
    });
  },

  // Apply translations to all [data-i18n] elements
  apply() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.dataset.i18n;
      const val = this.t(key);
      if (el.tagName === "INPUT" && el.placeholder !== undefined) {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    });
    // Placeholder-specific elements
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
    // Khmer font class
    document.documentElement.classList.toggle("lang-kh", this.current === "kh");
  },
};

// Auto-apply on load
document.addEventListener("DOMContentLoaded", () => i18n.apply());
